# to check the type of value stored in two different variables, if type of both the variable is same then print type of that variable else print the first variable.
def main():
    x = 110
    y = 1011.1
    if type(x) is type(y):
        print(type(x))
    else:
        print(type(x))


if __name__ == "__main__":
    main()
